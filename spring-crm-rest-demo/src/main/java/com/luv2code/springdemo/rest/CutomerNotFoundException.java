package com.luv2code.springdemo.rest;

public class CutomerNotFoundException extends Exception {

	public CutomerNotFoundException() {
	}

	public CutomerNotFoundException(String arg0) {
		super(arg0);
	}

	public CutomerNotFoundException(Throwable arg0) {
		super(arg0);
	}

	public CutomerNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CutomerNotFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
